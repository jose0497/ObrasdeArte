package Domain;

/**
 *
 * @author jose0
 */
public class Square extends Position{
    private int height;
    private int weight;
    
    public Square(int x, int y, int height, int weight) {
        super(x, y);
        this.height = height;
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
    
    
}
